#include <iostream>
using namespace std;

int main()
{
    // char is a data type that can store a single character
    char grade = 'A';

    // string is a data type that can store a string of characters
    string company = "Google";

    // int is a data type that can store an integer
    int age = 21;

    // float is a data type that can store a decimal number
    float cwa = 99.3;

    // double is a data type that can store a decimal number but can store more decimal places than float
    double gpa = 3.54;

    // bool is a data type that can store a boolean value (true or false)
    bool isTired = true;

    return 0;
}